#Exercise  6: Loops

# For loop to print numbers from 1 to 5
for i in range(1, 6):
	print(i)

#For loop to print each fav color
favorite_colors = ["blue", "green", "red", "yellow"]
for color in favorite_colors:
	print(color)

#While loop to print number from 10 down to 1

n = 10
while n > 0:
	print(n)
	n -= 1
